package main

import "fmt"

func main() {
	var age int = 40
	var num float64 = 1.618
	var pi float64 = 3.1415926
	fmt.Println(age, num)
	fmt.Printf("%d \n", 100) // integer
	fmt.Printf("%b \n", 100) // binary
	fmt.Printf("%c \n", 44)  // character
	fmt.Printf("%x \n", 17)  // hex
	fmt.Printf("%e \n", pi)  // scientific type
	fmt.Printf("hello, world\n")
}
